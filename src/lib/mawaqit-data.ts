// Helper function to get prayer times for a specific day and month
export function getPrayerTimesForDay(month: number, day: number) {
  const monthData = MAWAQIT_PRAYER_DATA.calendar[month];
  const dayKey = day.toString();
  if (!monthData || !monthData[dayKey as keyof typeof monthData]) {
    return null;
  }
  
  const times = monthData[dayKey as keyof typeof monthData];
  if (!times) {
    return null;
  }
  
  return {
    fajr: times[0],
    shuruq: times[1], 
    dhuhr: times[2],
    asr: times[3],
    maghrib: times[4],
    isha: times[5]
  };
}

// Helper function to get current day's prayer times
export function getCurrentPrayerTimes() {
  const today = new Date();
  const month = today.getMonth(); // 0-11
  const day = today.getDate(); // 1-31
  
  return getPrayerTimesForDay(month, day);
}

